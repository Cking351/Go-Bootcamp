 package main

 import "fmt"

 func main() {
	 // A single-precision floating point value
	 var batman, superman float32

	 // Typically a single octet (one byte)
	 var kitkat byte

	 // natural size for an integer
	 var i int

	 // true or false
	 var autumn bool

	  fmt.Println(batman, superman)
          fmt.Println(kitkat)
    	  fmt.Println(i)
    	  fmt.Println(autumn)
} 
